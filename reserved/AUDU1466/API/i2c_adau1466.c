static void delay(void)
{
    volatile uint32_t i = 0;
    for (i = 0; i < EXAMPLE_DELAY_COUNT; ++i)
    {
        __asm("NOP"); /* delay */
    }
}

/*
*@Function NAME：   uint32_t I2C_ADAU1466_Read(LPI2C_Type *base,uint16_t dev_addr,uint32_t subAdd, uint32_t dataLen,uint8_t* dataBuff)
*@description:     	I2C读ADAU1466
*@Author:           陈泽芳
*@param[out]:       无
*@param[in]:        LPI2C_Type *base，硬件I2C
*@param[in]:        uint16_t dev_addr，ADAU1466硬件I2C地址
*@param[in]:        uint32_t subAdd，ADAU1466寄存器地址
*@param[in]:        uint32_t dataLen，读出的数据长度
*@param[in]:        uint8_t* dataBuff，读出的数据buffer
*@return:           0，写数据成功；其他，读数据失败
*/
static status_t I2C_ADAU1466_Write(LPI2C_Type *base,uint16_t dev_addr,uint32_t subAdd,uint32_t dataLen,const uint8_t *dataBuff)
{
	  lpi2c_master_transfer_t xfer;
    status_t status;
		void *pbuf = (void *)dataBuff;
	
		xfer.slaveAddress = (dev_addr>>1);
    xfer.direction = kLPI2C_Write;
    xfer.subaddress = subAdd;
    xfer.subaddressSize = 0x02;
    xfer.data = pbuf;
    xfer.dataSize = dataLen;
    xfer.flags = kLPI2C_TransferDefaultFlag;

    status = LPI2C_MasterTransferBlocking(base, &xfer);

    return status;
}

/*
*@Function NAME：   uint32_t I2C_ADAU1466_Read(LPI2C_Type *base,uint16_t dev_addr,uint32_t subAdd, uint32_t dataLen,uint8_t* dataBuffer)
*@description:     	I2C读ADAU1466
*@Author:           陈泽芳
*@param[out]:       无
*@param[in]:        LPI2C_Type *base，硬件I2C
*@param[in]:        uint16_t dev_addr，ADAU1466硬件I2C地址
*@param[in]:        uint32_t subAdd，ADAU1466寄存器地址
*@param[in]:        uint32_t dataLen，读出的数据长度
*@param[in]:        uint8_t* dataBuffer，读出的数据buffer
*@return:           0，读数据成功；1，读数据失败
*/
static uint32_t I2C_ADAU1466_Read(LPI2C_Type *base,uint16_t dev_addr,uint32_t subAdd, uint32_t dataLen,uint8_t* dataBuffer)
{
	lpi2c_master_transfer_t masterXfer = {0};
	status_t reVal = kStatus_Fail;

	masterXfer.slaveAddress = (dev_addr>>1);
	masterXfer.direction = kLPI2C_Read;
	masterXfer.subaddress = subAdd;
	masterXfer.subaddressSize = 0x02;
	masterXfer.data = dataBuffer;
	masterXfer.dataSize = dataLen;
	masterXfer.flags = kLPI2C_TransferDefaultFlag;

	reVal = LPI2C_MasterTransferBlocking(base, &masterXfer);

	if (reVal != kStatus_Success)
	{
			return 1;
	}
	
	return 0;
}

