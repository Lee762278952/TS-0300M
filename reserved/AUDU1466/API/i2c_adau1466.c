									ADAU1466_MuteCtrl(DSP_DEVICE_1,REG_DSP1_CHANNEL_OUT_2_LINE1_ADDR,DSP_MUTE_ON);//
								break;
								
							case DSP_OUTPUT_IN2:			//IN 2输入源
								if(vol < DSP_VOLUME_N144_DB)
									ADAU1466_VolumeCtrl(DSP_DEVICE_1,REG_DSP1_CHANNEL_OUT_2_LINE2_ADDR,vol);//
