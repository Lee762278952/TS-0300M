/*
*@Function NAME：   static status_t ADAU1466_MonoCtrl(DspSysMode_E mode)
*@description:     	同传模式与其他模式切换控制
*@Author:           陈泽芳
*@param[out]:       无
*@param[in]:        DspSysMode_E mode，模式
*@return:           status
*/
static status_t ADAU1466_ModeMonoCtrl(DspSysMode_E mode)
{
	status_t status;
	uint8_t i;
	switch (mode)
	{
		case DSP_MODE_SI:
			ADAU1466_PageSelect(DSP_DEVICE_1,DSP_PAGE_1);
			for(i=0;i<15;i++)
				ADAU1466_MonoCtrl(DSP_DEVICE_1,REG_DSP1_CH2_MONO_SWITCH_ADDR+i,DSP_MONO_ON);
			ADAU1466_MonoCtrl(DSP_DEVICE_1,REG_DSP1_CH1_MONO_SWITCH_ADDR,DSP_MONO_ON);
			ADAU1466_PageSelect(DSP_DEVICE_1,DSP_PAGE_0);
			break;
		case DSP_MODE_WIRE:
		case DSP_MODE_PARTITION:
		case DSP_MODE_WIFI:
			ADAU1466_PageSelect(DSP_DEVICE_1,DSP_PAGE_1);
			for(i=0;i<15;i++)
				ADAU1466_MonoCtrl(DSP_DEVICE_1,REG_DSP1_CH2_MONO_SWITCH_ADDR+i,DSP_MONO_OFF);
			ADAU1466_MonoCtrl(DSP_DEVICE_1,REG_DSP1_CH1_MONO_SWITCH_ADDR,DSP_MONO_OFF);
			ADAU1466_PageSelect(DSP_DEVICE_1,DSP_PAGE_0);
			break;
		default:
			
			break;
	}
	return status;
}

