/*
*@Function NAME：   status_t ADAU1466_SetMode(DspSysMode_E mod)
*@description:     	模式设置
*@Author:           陈泽芳
*@param[out]:       无
*@param[in]:        DspSysMode_E mod
*@return:           status
*/
status_t ADAU1466_SetMode(DspSysMode_E mod)
{
	status_t status;
	switch (mod)
	{
		case DSP_MODE_WIRE:
			g_unDspInfo.mode = DSP_MODE_WIRE;
			status = ADAU1466_WireModeInit();
			break;
		
		case DSP_MODE_PARTITION:
			g_unDspInfo.mode = DSP_MODE_PARTITION;
			status = ADAU1466_PartitionModeInit();
			break;
		
		case DSP_MODE_SI:
			g_unDspInfo.mode = DSP_MODE_SI;
			status = ADAU1466_SIModeInit();
			break;
		
		case DSP_MODE_WIFI:
			g_unDspInfo.mode = DSP_MODE_WIFI;
			status = ADAU1466_WifiModeInit();
			break;
		
		default:
			status=kStatus_OutOfRange;
			break;
	}
	return status;
}

